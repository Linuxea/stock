# 纳斯达克
sed -i 's|22,668.21|22,748.86|' "$FILE"
sed -i 's|▼ 210.17|▲ 80.65|' "$FILE"
sed -i 's|-0.92%|+0.36%|' "$FILE"
sed -i 's|科技股承压，英伟达等AI龙头延续跌势，纳斯达克跌近1%|科技股反弹，投资者逢低买入，纳斯达克收涨|' "$FILE"


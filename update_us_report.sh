#!/bin/bash
FILE="20260303/us_stock_report.html"

# 标题
sed -i 's|<title>2026年2月27日 美股市场行情报告</title>|<title>2026年3月3日 美股市场行情报告</title>|' "$FILE"

# 头部日期
sed -i 's|2026年2月27日 星期四 | 美国东部时间|2026年3月3日 星期二 | 美国东部时间|' "$FILE"

# 更新时间
sed -i 's|2026-02-27 16:00 ET|2026-03-02 16:00 ET|' "$FILE"

# 道琼斯数据 - 需要多行替换，这里简化处理
sed -i 's|48,977.92|48,904.78|' "$FILE"
sed -i 's|▼ 521.28|▼ 73.14|' "$FILE"
